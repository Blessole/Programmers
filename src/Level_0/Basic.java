package Level_0;

// 나머지 구하기
public class Basic {
    public int solution(int num1, int num2){
        int answer = 0;

        // 합
        answer = num1+num2;

        // 차
        answer =  num1-num2;

        // 곱
        answer =  num1*num2;

        // 몫
        answer = num1/num2;

        // 나머지
        answer =   num1%num2;

        return answer;
    }
}
